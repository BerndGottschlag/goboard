///Modifier bits in the HID modifier byte.
pub enum ModifierKey {
    /// Left control key.
    LeftControl = 0x1,
    /// Left shift key.
    LeftShift = 0x2,
    /// Left alt key.
    LeftAlt = 0x4,
    /// Left meta key.
    LeftMeta = 0x8,
    /// Right control key.
    RightControl = 0x10,
    /// Right shift key.
    RightShift = 0x20,
    /// Right alt key.
    RightAlt = 0x40,
    /// Right meta key.
    RightMeta = 0x80,
}

